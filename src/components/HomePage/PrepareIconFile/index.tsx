export const PrepareIconFile = ({ ext }: { ext: any }) => {
    if (ext == "docx") {
        return <WordIcon />;
    }
    if (ext == "jpg" || ext == "png") {
        return <ImageIcon />;
    }
    if (ext == "pdf") {
        return <PdfIcon />;
    } else {
        return <TextIcon text={ext.toUpperCase()} />;
    }
};
